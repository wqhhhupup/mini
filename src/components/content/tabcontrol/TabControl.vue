<template>
    <div class="tab-control">
      <div v-for="(item,index) in titles"
      :class="{active:index === currentIndex}" @click="tabControl(index)">
        <span>{{item}}</span>
      </div>
    </div>
</template>

<script>
    export default {
        name: "TabControl",
      props:{
          titles:{
            type:Array,
            default(){
              return []
            }
          }
      },
      data(){
        return{
          currentIndex:0
        }
      },
      methods:{
        tabControl(index){
          this.currentIndex = index;
          this.$emit('tabClick',index)
        }
      }
    }
</script>

